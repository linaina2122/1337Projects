#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	size_t	a;

	a = 0;
	while ((s1[a] || s2[a]))
	{
		if (s1[a] != s2[a])
			return (1);
		a++;
	}
	return (0);
}
